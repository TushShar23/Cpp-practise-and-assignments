#include<iostream>
using namespace std;

int main(){
    int a=100,b=200;

    cout << "a > b : " << (a>b) << endl;
    cout << "a < b : " << (a<b) << endl;
    cout << "a == b : " << (a==b) << endl;
    cout << "a != b : " << (a!=b) << endl;
    cout << "a >= b : " << (a>=b) << endl;
    cout << "a <= b : " << (a<=b) << endl;
    return 0;
}